/**
 * Optional LLM feedback step (--feedback flag).
 *
 * Sends failing criteria grouped by frequency to OLLAMA_FEEDBACK_MODEL
 * and writes the response to llm-feedback.md in the run directory.
 * This is advisory only — not part of the deterministic score.
 */

import { writeFileSync } from "fs";
import { join } from "path";
import { LmClient } from "./lm-client.ts";
import type { RunResult } from "./types.ts";

export async function writeLlmFeedback(
  runDir: string,
  result: RunResult,
  skillContent: string
): Promise<void> {
  const feedbackModel = process.env.OLLAMA_FEEDBACK_MODEL ?? process.env.OLLAMA_MODEL;
  if (!feedbackModel) {
    console.warn("  [feedback] No model configured — skipping (set OLLAMA_FEEDBACK_MODEL or OLLAMA_MODEL)");
    return;
  }

  // Collect all failed criteria across scenarios, grouped by name+type
  const failCounts = new Map<string, { count: number; scenarioIds: string[] }>();
  for (const s of result.scenarios) {
    for (const c of s.criteria) {
      if (!c.passed) {
        const key = `[${c.type}] ${c.name}`;
        const existing = failCounts.get(key) ?? { count: 0, scenarioIds: [] };
        existing.count++;
        existing.scenarioIds.push(s.id);
        failCounts.set(key, existing);
      }
    }
  }

  if (failCounts.size === 0) {
    const path = join(runDir, "llm-feedback.md");
    writeFileSync(path, `# LLM Feedback\n\n_No failing criteria — no feedback needed._\n`, "utf-8");
    return;
  }

  // Format failing criteria sorted by frequency
  const sorted = [...failCounts.entries()].sort((a, b) => b[1].count - a[1].count);
  const failureList = sorted
    .map(([name, { count, scenarioIds }]) =>
      `- ${name} — failed in ${count} scenario(s): ${scenarioIds.join(", ")}`
    )
    .join("\n");

  const overallPct = (result.summary.overall.passRate * 100).toFixed(1);

  const prompt = `You are reviewing a skill document used to teach AI models to interact with a REST API.
The skill was evaluated against ${result.scenarios.length} scenarios and achieved an overall pass rate of ${overallPct}%.

The following criteria FAILED most frequently (sorted by frequency):

${failureList}

Here is the full skill document:

<skill>
${skillContent}
</skill>

In 1–3 concise bullet points, suggest the most impactful changes to the skill document that would likely fix these failures.
Reference specific section headings or sentences from the skill when possible. Focus only on changes that address the listed failures.`;

  console.log(`  [feedback] Requesting feedback from ${feedbackModel}...`);

  const client = new LmClient({ model: feedbackModel });
  let feedbackText: string;
  try {
    const response = await client.complete([
      { role: "system", content: "You are a technical writing expert. Be concise and specific." },
      { role: "user", content: prompt },
    ], feedbackModel);
    feedbackText = response.content;
  } catch (err) {
    feedbackText = `_Error calling feedback model: ${err instanceof Error ? err.message : String(err)}_`;
  }

  const output = [
    `# LLM Feedback`,
    ``,
    `> **Advisory only.** This feedback was generated by \`${feedbackModel}\` based on failing eval criteria.`,
    `> It is not part of the deterministic score and may be inaccurate.`,
    ``,
    `**Model**: ${feedbackModel}  `,
    `**Run**: ${result.runId}  `,
    `**Overall pass rate**: ${overallPct}%`,
    ``,
    `## Top Failing Criteria`,
    ``,
    failureList,
    ``,
    `## Suggested Improvements`,
    ``,
    feedbackText,
  ].join("\n");

  writeFileSync(join(runDir, "llm-feedback.md"), output, "utf-8");
  console.log(`  [feedback] Written to llm-feedback.md`);
}
